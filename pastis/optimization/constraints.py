def kl_divergence(p, q):
    mask = (p != 0)
    if mask.sum() == mask.size:
        tmp = p * ag_np.log(p / q)
    else:
        tmp = p[mask] * ag_np.log(p[mask] / q[mask])
    return ag_np.sum(tmp)


def js_divergence(p, q):
    m = (p + q) / 2
    return (kl_divergence(p, m) + kl_divergence(q, m)) / 2


